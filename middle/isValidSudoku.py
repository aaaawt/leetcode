def isValidSudoku(board):
    row = [set() for i in range(9)]
    col = [set() for i in range(9)]
    squ = [set() for i in range(9)]
    for i in range(9):
        for j in range(9):
            x = board[i][j]
            cur = (i // 3) * 3 + j // 3
            if x != '.':
                if x not in row[i] and x not in col[j] and x not in squ[cur]:
                    row[i].add(x)
                    col[j].add(x)
                    squ[cur].add(x)
                else:
                    return False
    return True

